    private void jTabbedPane2StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane2StateChanged
        if (jTabbedPane2.getSelectedIndex() == 1) {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery("from Contacto c order by c.idContacto asc");
            List resultList = q.list();
            jComboBox3.removeAllItems();
            jComboBox4.removeAllItems();
            for (Object object : resultList) {
                Contacto c = (Contacto) object;
                jComboBox3.addItem(c.getIdContacto());
                jComboBox4.addItem(c.getIdContacto());
            }
            session.getTransaction().commit();
        } else if (jTabbedPane2.getSelectedIndex() == 2) {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery("from Contacto c order by c.idContacto asc");
            List resultList = q.list();
            jComboBox5.removeAllItems();
            for (Object object : resultList) {
                Contacto c = (Contacto) object;
                jComboBox5.addItem(c.getIdContacto());
            }
            session.getTransaction().commit();
        }
    }//GEN-LAST:event_jTabbedPane2StateChanged

    private Vector<Object> getGenericRowTable(Object o) {
        Vector<Object> oneRow = new Vector<Object>();
        if (o instanceof Contacto) {
            Contacto c = (Contacto) o;
            oneRow.add(c.getIdContacto());
            oneRow.add(c.getNombre());
            oneRow.add(c.getCorreo());
            oneRow.add(c.getTelefono());
        } else if (o instanceof Directorio) {
            Directorio d = (Directorio) o;
            oneRow.add(d.getContacto().getIdContacto());
            oneRow.add(d.getContacto1().getIdContacto());
            oneRow.add(d.getFechaConocido());
        } else if (o instanceof Seguro) {
            Seguro s = (Seguro) o;
            oneRow.add(s.getId());
            oneRow.add(s.getIdContacto());
            oneRow.add(s.getDescripcion());
        }
        return oneRow;
    }

    private Vector<String> getGenericHeaderTable(Object o) {
        Vector<String> header = new Vector<String>();
        if (o instanceof Contacto) {
            header.add("Id");
            header.add("Nombre");
            header.add("Correo");
            header.add("Teléfono");
        } else if (o instanceof Directorio) {
            header.add("Id Contacto 1");
            header.add("Id Contacto 2");
            header.add("Fecha Conocido");
        } else if (o instanceof Seguro) {
            header.add("Id");
            header.add("Id Contacto");
            header.add("Descripción");
        }
        return header;
    }

    private void displayResult(List resultList) {
        if (resultList.isEmpty()) {
            jTable1.setModel(new DefaultTableModel(new Vector(), new Vector()));
            JOptionPane.showMessageDialog(null, "No se encontraron registros");
            return;
        }

        Vector<String> tableHeaders = getGenericHeaderTable(resultList.get(0));
        Vector tableData = new Vector();

        for (Object o : resultList) {
            tableData.add(getGenericRowTable(o));
        }

        jTable1.setModel(new DefaultTableModel(tableData, tableHeaders));
    }

